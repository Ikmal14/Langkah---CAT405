#data collection for MRT and LRT stations
class Station:
    def __init__(self, name, lat, lon):
        self.name = name
        self.lat = lat
        self.lon = lon
        self.connections = {}

    def addConnection(self, station, line):
        self.connections[station] = line

class Line:
    def __init__(self, name):
        self.name = name
        self.stations = []

    def addStation(self, station):
        self.stations.append(station)
     

